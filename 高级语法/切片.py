# 访问
x = [1,2,3,4,5,6,7,8,10,11]
print(x[4])
print(x[3:])
print(x[::2])
print(x[::-1]) # 逆序

# 插入
x[3:] = [55,66,77,88,99]
print(x)            # 在指定位置之后替换
x[3:3] = [4869]     # 在指定位置中间插入一个值
print(x)
x[:0] = [4453]      # 在首部插入元素
print(x)
x[::2] = [33,33,33,33,33] # 步长为2，替换对应位置的元素。要注意替换元素的个数要与被替换的一致
print(x)

# 删除
x[:5] = []  # 删除从头到5的位置的元素
print(x)
del x[3:]   # 删除从3位置到最后的元素
print(x)
y = [234,123,43,51,545,1423,4234]
del y[::2]
print(y)    # 隔俩删除一个